package com.lkb.bean;

import java.math.BigDecimal;
import java.util.Date;

public class Order {
	private String id;
	private String orderId;//订单ID
	private String productNames;//商品名称集合
	private String receiver;//收货人
	private BigDecimal money;//订单金额
	private String buyway;//购买方式
	private Date buyTime;//下单时间
	private String orderstatus;//订单状态
	private String ordersource;//订单来源
	private String loginName;//loginName
	private String recevierAddr;//收货地址
	private String recevierPost; //收货人邮编
	private String recevierFixPhone; //收货人固定电话
	private String recevierTelephone; //收货人手机号
	private String recevierEmail;//收货人email


	
	
	public String getProductNames() {
		return productNames;
	}
	public void setProductNames(String productNames) {
		this.productNames = productNames;
	}
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getReceiver() {
		return receiver;
	}
	public void setReceiver(String receiver) {
		this.receiver = receiver;
	}

	public BigDecimal getMoney() {
		return money;
	}
	public void setMoney(BigDecimal money) {
		this.money = money;
	}
	public String getBuyway() {
		return buyway;
	}
	public void setBuyway(String buyway) {
		this.buyway = buyway;
	}
	public Date getBuyTime() {
		return buyTime;
	}
	public void setBuyTime(Date buyTime) {
		this.buyTime = buyTime;
	}
	public String getOrderstatus() {
		return orderstatus;
	}
	public void setOrderstatus(String orderstatus) {
		this.orderstatus = orderstatus;
	}

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOrdersource() {
		return ordersource;
	}
	public void setOrdersource(String ordersource) {
		this.ordersource = ordersource;
	}
	public String getLoginName() {
		return loginName;
	}
	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}
	public String getRecevierAddr() {
		return recevierAddr;
	}
	public void setRecevierAddr(String recevierAddr) {
		this.recevierAddr = recevierAddr;
	}
	public String getRecevierFixPhone() {
		return recevierFixPhone;
	}
	public void setRecevierFixPhone(String recevierFixPhone) {
		this.recevierFixPhone = recevierFixPhone;
	}
	public String getRecevierTelephone() {
		return recevierTelephone;
	}
	public void setRecevierTelephone(String recevierTelephone) {
		this.recevierTelephone = recevierTelephone;
	}
	public String getRecevierEmail() {
		return recevierEmail;
	}
	public void setRecevierEmail(String recevierEmail) {
		this.recevierEmail = recevierEmail;
	}
	
	public String getRecevierPost() {
		return recevierPost;
	}
	public void setRecevierPost(String recevierPost) {
		this.recevierPost = recevierPost;
	}
	@Override
	public String toString() {
		return "Order [id=" + id + ", orderId=" + orderId + ", productNames="
				+ productNames + ", receiver=" + receiver + ", money=" + money
				+ ", buyway=" + buyway + ", buyTime=" + buyTime
				+ ", orderstatus=" + orderstatus + ", ordersource="
				+ ordersource + ", loginName=" + loginName + ", recevierAddr="
				+ recevierAddr + ", recevierPost=" + recevierPost
				+ ", recevierFixPhone=" + recevierFixPhone
				+ ", recevierTelephone=" + recevierTelephone
				+ ", recevierEmail=" + recevierEmail + "]";
	}

	
}
